import java.util.ArrayList;

public class WrapperClass {

	public static void main(String[] args) {
		
		char ch='a'; //primitive data type
		
		Character ch1=ch;//Autoboxing : Converting primitive to Wrapper class object.
		
		System.out.println(ch1);
		
		Integer a=19;//Wrapper class object
		
		int a1=a; //Unboxing : Converting Wrapper class object to promitive data type.
		
		System.out.println(a1);
		
		

	}

}
